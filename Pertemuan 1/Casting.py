#Specify a Variable Type/Menetapkan Tipe Variabel
x = int(7)  # x will be 7
y = int(2.2)# y will be 2
z = int("9") # z will be 9
print(x)
print(y)
print(z)

a = float(76)   # a will be 76.0
b = float(2.2)  # b will be 2.2
c = float("9")   # c will be 9.0
d = float(7.2) # d will be 7.2
print(a)
print(b)
print(c)
print(d)

e = str("y1") # e will be 'y1'
f = str(8)    # f will be '8'
g = str(5.0)  # g will be '5.0'
print(e)
print(f)
print(g)